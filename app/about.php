            <p>We are the Broad Street Angels, founded by a small group of members of The <a target="_blank" href='http://unionleague.org'>Union League of Philadelphia</a> who gathered in the Spring of 2016 to create an affinity club with the purpose of driving investment to startups in the Mid-Atlantic region.</p>
            <p>The Broad Street Angels has grown to nearly 100 Union League of Philadelphia members with highly successful backgrounds in numerous industries ranging from retail, healthcare, medical devices, technology, finance, real estate, intellectual property law, entertainment, retail and more.  We are current or retired CEOs and C-Suite executives, patent attorneys, investment fund managers, bankers, CPAs, executives from Fortune 500 companies, technologists, physicians and entrepreneurs. 49 of our members identify themselves as entrepreneurs, while 19 are active CEOs and 6 have founded their own firms.</p>

            <p>As an affinity club of The Union League of Philadelphia, the Broad Street Angels are exclusively Union League members that qualify as accredited investors. Investments are made by individuals in amounts that range from $25,000 to $1 million or more.
