            <h2>About the Union League</h2>
            <p>Founded in 1862 as a patriotic society to support the Union and the policies of President Abraham Lincoln, The Union League of Philadelphia laid the philosophical foundation of other Union Leagues across a nation torn by civil war. The League has hosted US presidents, heads of state, industrialists, entertainers and dignitaries from around the globe and has proudly supported the American military in each conflict since the Civil War. The Union League continues to be driven by its motto, Amor Patriae Ducit or Love of Country Leads.</p>

            <p>Today, The Union League is home to over 3,500 men and women who contribute to club life and keep alive the League's traditions. As they did in 1862, today's members represent the Philadelphia region's leaders in business, education, technology, healthcare, law, government, religion, art and culture. The League's civic participation and philanthropic outreach takes the form of three charitable foundations: The Youth Work Foundation, The Scholarship Foundation and The Abraham Lincoln Foundation, which educate the public about our nation's history, recognize student role models in our region's schools, and provide scholarships to deserving students.</p>

            <h2>Why work with the Broad Street Angels?</h2>
            <p>We are on your side. We understand the demands and stress of our startup companies and want to make sure our due diligence process is thorough but not overbearing. The last thing we want to do is distract you from focusing on growing your business because it doesn’t help anyone’s investment be successful. We like to think that an investment from a BSA member brings a network of contacts and experience that can help you grow your company. We don’t just bring dollars. We bring brain power.</p>
            <p>Many of our members often help to make introductions to prospective customers, partners, legal and financial resources. Members are asked to serve on the board of directors or advisory boards of our portfolio companies, or provide consulting services to help our companies get to the next milestone.</p>
            <p>Once we invest we want you to succeed and help where we can. We even help companies that we don’t invest in. It might be that the company needs to hit a milestone, tighten up their sales strategy or show progress from a recent pivot. We often provide constructive feedback when we don’t invest so that our companies understand our decision and can focus on areas that could make us interested down the road.</p>
            <p>Each Broad Street Angel member is an accredited investor with a successful track record in their respective industries. Our members bring senior level expertise to each of our portfolio companies. </p>
