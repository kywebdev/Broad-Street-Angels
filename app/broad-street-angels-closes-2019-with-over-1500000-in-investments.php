              <p>(Philadelphia, PA) February 20, 2020 - Broad Street Angels (BSA), an angel investment group in Philadelphia, PA, held its annual meeting at The <a href="https://www.unionleague.org" target="_blank">Union League of Philadelphia</a> on Thursday February 20, 2020 to announce its annual results. BSA invested over $1.5 million in 2019. Members funded 7 new investments and 5 existing portfolio companies.</p>
              <p>“The breadth of our experience is shown in the diversity of the Mid-Atlantic based companies that we added to our portfolio this year. Our new tagline, ‘We fuel success’, tells it all,” said RoseAnn Rosenthal, Chair of the Broad Street Angels and President of Ben Franklin Technology Partners of Southeastern Pennsylvania. New investments were made into:</p>
              <p><a href="https://www.bounceimaging.com/" target="_blank">Bounce Imaging</a> - 360 degree cameras for tactical and rescue applications MNI Nutrition - the most efficient way of delivering calories, 'good' long chain fats and precision nutrients, without requiring digestion.</p>
              <p><a href="https://www.keriton.com/" target="_blank">Keriton</a> - a new gold standard for feeding management, lactation analytics and patient engagement platform for the NICU.</p>
              <p><a href="https://narbis.com/" target="_blank">Narbis</a> - medical-tech to help people learn to focus & improve their attention. It helps ADHD, Dementia, Insomnia, PTSD and other brain disorders.</p>
              <p><a href="https://www.routedynamicscorp.com/" target="_blank">Route Dynamics Corp</a> - its SmartRoutes service is similar to the “Faster Route Found” function on Waze (G-Maps) for autos, except with more efficient routes for aircraft.</p>
              <p><a href="https://thrivecommerce.com/" target="_blank">Thrive Commerce</a> - built the first AI-promotions platform for merchants to author and distribute online and in-store promotions to shoppers</p>
              <p><a href="https://www.triplebristle.com/" target="_blank">Triple Bristle</a> - an oral care brand that sells 3-sided sonic toothbrushes protected by two utility patents</p>
              <p>Add on investments were made into <a href="http://www.zsxmedical.com/" target="_blank">ZSX Medical</a>, <a href="https://www.neuroflowsolution.com/" target="_blank">NeuroFlow Solutions</a>, <a href="https://www.sayeureqa.com/" target="_blank">eureQa</a>, <a href="https://houwzer.com/" target="_blank">Houwzer</a> and <a href="https://www.mytonomy.com/home.html" target="_blank">Mytonomy</a>.</p>
              <p>BSA’s new logo and website at <a href="http://www.broadstreetangels.com/" target="_blank">www.broadstreetangels.com</a> were also unveiled at the meeting with the objective of recruiting the best successful startups across the Mid Atlantic to apply for funding. The new logo and website were built by <a href="https://pavone.net/" target="_blank">Pavone Marketing</a>, an advertising and marketing firm with offices in Philadelphia, Harrisburg and Chicago.</p>
              <p>Since its first member meeting in 2017, the Broad Street Angels have invested nearly $5 million in 18 companies.</p><br /><br />
              <p><strong>About Broad Street Angels</strong> (<a href="http://www.broadstreetangels.com/" target="_blank">www.broadstreetangels.com</a>)</p>
              <p>Founded in 2017, Broad Street Angels is an affinity club at The Union League of Philadelphia.  It consists of nearly 100 Union League members who actively invest in early-stage enterprises in the Mid-Atlantic to fuel success in regional growth.   Broad Street Angels is a member of ACA, PACT, PA Angel Network, and Mid Atlantic Diamond Ventures.</p>
